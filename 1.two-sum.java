// @lc code=start
class Solution {
    public int[] twoSum(int[] nums, int target) {
        //create a double loop, first loop picks lead element, second loop adds lead element to each other element and checks to see if it returnes target
        
        for(int i = 0; i < nums.length - 1; i++){
            for(int j = i + 1; j < nums.length; j++){
                if(nums[i] + nums[j] == target){
                    return new int[]{i, j};
                }
            }
        }
        throw new IllegalArgumentException("No two sum solution");
    }
}
